CREATE TABLE IF NOT EXISTS SIMULATION.ACCOUNT (
  ID INT AUTO_INCREMENT  PRIMARY KEY,
  AGENCY VARCHAR(10) NOT NULL,
  NUMBER varchar(10) NOT NULL,
  DIGIT varchar(3) NOT NULL,
  ACCOUNT_TYPE int NOT NULL,
  BALANCE DECIMAL(19,5) default 0,
  BANK_FK int NOT NULL,
  foreign key (BANK_FK) references SIMULATION.BANK(ID)
);

